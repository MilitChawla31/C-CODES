//SIZE OF ALL DATA TYPES
#include<stdio.h>
int main()
{
    int i;
    char ch;
    long int li;
    float f;
    double d;
    printf("Size of all Datatypes \n");
    printf("Size of Integer Datatype is  = %d \n", sizeof(i));
    printf("Size of Character Datatype is = %d \n", sizeof(ch));
    printf("Size of Long Integer Datatype is  = %d \n", sizeof(li));
    printf("Size of Float Datatype is = %d \n", sizeof(f));
    printf("Size of Double Datatype is = %d \n", sizeof(d));
    return 0;
}

